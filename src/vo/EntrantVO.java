package vo;

public class EntrantVO {
	String startdate;
	String finishdate;
	int round;
	String names;
	int entrant_seq;
	
	public String getStartdate() {
		return startdate;
	}
	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}
	public String getFinishdate() {
		return finishdate;
	}
	public void setFinishdate(String finishdate) {
		this.finishdate = finishdate;
	}
	public int getRound() {
		return round;
	}
	public void setRound(int round) {
		this.round = round;
	}
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	public int getEntrant_seq() {
		return entrant_seq;
	}
	public void setEntrant_seq(int entrant_seq) {
		this.entrant_seq = entrant_seq;
	}
	
	
}
